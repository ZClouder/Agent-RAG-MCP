@dataclass(frozen=True)
class RetrievalProfile:
    """Retrieval hints generated by task routing.

    These hints are metadata for the caller and future tuning. V1 does not hard
    override HybridSearch internals with them because the current collection
    metadata may not reliably contain document-type labels.
    """

    dense_top_k: int
    sparse_top_k: int
    fusion_top_k: int
    rerank_preferred: bool = True

    def to_dict(self) -> Dict[str, Any]:
        return asdict(self)


@dataclass(frozen=True)
class BusinessGraphNode:
    """A lightweight business graph node for one content-ops task."""

    intent: str
    label: str
    keywords: List[str]
    preferred_doc_types: List[str]
    focus_terms: List[str]
    evidence_requirements: List[str]
    retrieval_profile: RetrievalProfile
    related_intents: List[str] = field(default_factory=list)

    def to_dict(self) -> Dict[str, Any]:
        data = asdict(self)
        data["retrieval_profile"] = self.retrieval_profile.to_dict()
        return data


@dataclass(frozen=True)
class QueryRouteDecision:
    """Routing result used by query_knowledge_hub."""

    original_query: str
    rewritten_query: str
    intent: str
    intent_label: str
    confidence: float
    matched_terms: List[str]
    preferred_doc_types: List[str]
    focus_terms: List[str]
    evidence_requirements: List[str]
    retrieval_profile: RetrievalProfile

    def to_dict(self) -> Dict[str, Any]:
        data = asdict(self)
        data["retrieval_profile"] = self.retrieval_profile.to_dict()
        return data


def _dedupe(items: Iterable[str]) -> List[str]:
    seen = set()
    result = []
    for item in items:
        key = item.lower()
        if key in seen:
            continue
        seen.add(key)
        result.append(item)
    return result


