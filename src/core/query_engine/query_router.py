class TaskQueryRouter:
    """Classify content-ops task intent and rewrite query for retrieval."""

    def __init__(
        self,
        graph: Mapping[str, BusinessGraphNode] | None = None,
        min_confidence: float = 0.18,
    ) -> None:
        self.graph = dict(graph or default_business_graph())
        self.min_confidence = min_confidence

    def route(self, query: str) -> QueryRouteDecision:
        """Classify and rewrite a user query.

        Args:
            query: Original user query.

        Returns:
            Deterministic route decision. Empty queries route to general.
        """
        original = query or ""
        normalized = self._normalize(original)
        if not normalized:
            return self._general(original)

        scored = [
            (intent, *self._score_node(normalized, node))
            for intent, node in self.graph.items()
        ]
        scored.sort(key=lambda item: (item[1], len(item[2])), reverse=True)
        best_intent, best_score, matched_terms = scored[0]
        total_score = sum(item[1] for item in scored)
        confidence = best_score / max(1.0, total_score + 1.0)

        if best_score <= 0 or confidence < self.min_confidence:
            return self._general(original)

        node = self.graph[best_intent]
        return QueryRouteDecision(
            original_query=original,
            rewritten_query=self._rewrite(original, node),
            intent=node.intent,
            intent_label=node.label,
            confidence=round(confidence, 4),
            matched_terms=matched_terms,
            preferred_doc_types=list(node.preferred_doc_types),
            focus_terms=list(node.focus_terms),
            evidence_requirements=list(node.evidence_requirements),
            retrieval_profile=node.retrieval_profile,
        )

    @staticmethod
    def _normalize(query: str) -> str:
        return " ".join(query.strip().lower().split())

    @staticmethod
    def _score_node(query: str, node: BusinessGraphNode) -> tuple[float, List[str]]:
        score = 0.0
        matched = []
        for keyword in node.keywords:
            keyword_norm = keyword.lower()
            if not keyword_norm:
                continue
            if keyword_norm in query:
                matched.append(keyword)
                score += 2.0 if len(keyword_norm) > 1 else 1.0

        # Extra phrase-aware boosts for common mixed task queries.
        if node.intent == "script_compliance" and re.search(r"脚本.*(合规|风险|品牌规范|违规)", query):
            score += 3.0
        if node.intent == "product_verification" and re.search(r"产品.*(核查|准确|参数|卖点)", query):
            score += 3.0
        if node.intent == "review_optimization" and re.search(r"(复盘|历史).*(优化|转化|原因)", query):
            score += 3.0

        return score, _dedupe(matched)

    def _general(self, query: str) -> QueryRouteDecision:
        profile = RetrievalProfile(20, 20, 10, True)
        return QueryRouteDecision(
            original_query=query,
            rewritten_query=query,
            intent="general",
            intent_label="general knowledge retrieval",
            confidence=0.0,
            matched_terms=[],
            preferred_doc_types=[],
            focus_terms=[],
            evidence_requirements=["cite_sources"],
            retrieval_profile=profile,
        )

    @staticmethod
    def _rewrite(query: str, node: BusinessGraphNode) -> str:
        parts = [
            query.strip(),
            f"业务场景:{node.label}",
            f"优先资料:{' '.join(node.preferred_doc_types)}",
            f"关注点:{' '.join(node.focus_terms)}",
            f"证据要求:{' '.join(node.evidence_requirements)}",
        ]
        return "\n".join(part for part in parts if part)
