"""Task-oriented query routing for consumer-brand content operations.

The router is intentionally deterministic. It uses a small business graph to
map operational intents to preferred document types, focus terms, evidence
requirements, and compact query rewrites before the existing Hybrid Search
pipeline runs.
"""

